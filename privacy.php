<script>
    lucide.createIcons();
    
    // Breadcrumb or Scroll Tracking (Optional but makes it look premium)
    document.addEventListener('DOMContentLoaded', function() {
        const sections = document.querySelectorAll('.scroll-mt-32');
        const navLinks = document.querySelectorAll('nav a');

        window.addEventListener('scroll', () => {
            let current = "";
            sections.forEach((section) => {
                const sectionTop = section.offsetTop;
                if (pageYOffset >= sectionTop - 150) {
                    current = section.getAttribute("id");
                }
            });

            navLinks.forEach((link) => {
                link.classList.remove("bg-blue-50", "text-blue-600", "border-blue-600");
                if (link.getAttribute("href") === `#${current}`) {
                    link.classList.add("bg-blue-50", "text-blue-600", "border-blue-600");
                }
            });
        });
    });
</script>
